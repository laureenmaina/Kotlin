//Arithmetic Operators
fun main(){
    var fnum:Int=25
    var snum:Int=6

    println(fnum+snum)
    println(fnum-snum)
    println(fnum*snum)
    println(fnum/snum)
    println(fnum%snum) //Modulus

    //Assignment Operators
    var x=5
    println(x)

    var y=3
    y+=7
    println(y)

    var a=78
    a/=5
    println(a)
}

